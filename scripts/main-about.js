function checkSize(event)
{
    const dynamicBodySize = document.body.clientWidth;
    console.log("size changed to: ", dynamicBodySize);
    if (dynamicBodySize <= 281)
    {
        switchIcons4();
    }   
    else if (dynamicBodySize >= 282 && dynamicBodySize <= 1030)
    {
        switchIcons3();
    }
    else if (dynamicBodySize >= 1031 && dynamicBodySize <= 1261)
    {
        switchIcons1();
    }
    else if (dynamicBodySize > 1261)
    {
        switchIcons2();
    }
}
window.addEventListener("resize", checkSize);
